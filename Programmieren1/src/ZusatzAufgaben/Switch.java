package ZusatzAufgaben;

public class Switch
{
	
	public static void main(String[] args)
	{
		
		String monat = "jul";
		
		switch (monat) {
		case "jan":
			System.out.println(1);
		case "jul":
			System.out.println(2);
		default:
			System.out.println(3);
					
		}
		
	}

}
